package com.braz.questoes;

public class Questao6 {
    /*Desenvolva um programa que mostre
    todos os valores entre 0 e 10 em
    ordem crescente (utilizando um ciclo FOR)*/

    public void cicloDecrescente(){
        int i;
        for(i = 10; i>0; i--) {
            System.out.println(i);
        }
    }
}
